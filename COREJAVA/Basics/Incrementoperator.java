package in.co.Basics;

public class Incrementoperator {
	
	 public static void main(String[] args) {
	 System.out.println("Increment opertor" +  "\n");	 
		 
		 int a=10;
		 
		 System.out.println ("a is " + a + "\n" ); //a=10
		  
		 int b=++a;   //pre increment
		 
		 System.out.println("a is " + a + "\n" );  //a=11
		 System.out.println("b is " + b + "\n" );  //b=11
		 
		 
		 int A=10;
		 
		 System.out.println("A is " + A + "\n" );  //A=10
		 
		 int B=A++;   //post increment
		 
		 System.out.println("A is " + A + "\n" );  //A=11
		 System.out.println("B is " + B + "\n" );  //B=10

		 
	 }
}
