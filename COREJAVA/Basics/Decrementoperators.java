package in.co.Basics;

public class Decrementoperators {
	
	 public static void main(String[] args) {
	 System.out.println("Decrement opertor ");
		
		 int a=20;
		 
		 System.out.println("a is " + a);  //a=20
	 	 
		 int b=--a;   //pre decrement
		
		 System.out.println("b is " + b);  //b=19
		 
		 
		 int A=20;
		 
		 System.out.println("A is " + A);  //A=20
		 
		 int B=A--;   //post decrement
		 
		 System.out.println("B is " + B);  //B=20
		 
		 
		 
	 }
	 }